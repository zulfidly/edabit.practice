function lessThan100(a, b) {
	if (a+b < 100) {
        return true }
    else {  
        return false  }
}

console.log(lessThan100(22, 15)) //➞ true
// 22 + 15 = 37

console.log(lessThan100(83, 34)) //➞ false
// 83 + 34 = 117

console.log(lessThan100(3, 77)) //➞ true